package decisionmaking_if;

public class DecisionMaking_If {

    public static void main(String[] args) {
        int x = 12;
        if (x <13){
            System.out.println("Angka ini kurang dari 13");
        }
        System.out.println("============================================================");
        System.out.println("                Decision Making If 1");
        System.out.println("============================================================");
        
        int n = 10;
        if (n<=20){
            System.out.println("Nilai ini lebih kecil atau sama dengan 20");
        }
        System.out.println("============================================================");
        System.out.println("                Decision Making If 2");
        System.out.println("============================================================");
    }
    
}
