package looping_tugas2;

public class For {
    
    public static void main(String[] args) {        
        for(int x = 0; x < 15; x++) {
         System.out.println("Urutan ke - " +x );
        }
            System.out.println("=================================================================");
            System.out.println("                            Looping For 1");
            System.out.println("=================================================================");
            
        for(int i = 0; i < 13; i=i+2) {
         System.out.println("Bilangan Genap : " +i );
        }
            System.out.println("=================================================================");
            System.out.println("                            Looping For 2");
            System.out.println("=================================================================");
            
        for(int i = 1; i <=5 ; i++) {
         System.out.print(i+ ".");
         System.out.println("Niken");
        }
            System.out.println("=================================================================");
            System.out.println("                            Looping For 3");
            System.out.println("=================================================================");
            
        for(int i = 4; i >=0 ; i--) {
         System.out.print(i+ ".");
         System.out.println("Nomor Berkurang");
        }
            System.out.println("=================================================================");
            System.out.println("                            Looping For 4");
            System.out.println("=================================================================");
    }
}
